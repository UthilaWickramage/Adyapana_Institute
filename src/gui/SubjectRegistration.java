    public void loadSubjects() {

        try {
            ResultSet rs = MySQL.search("SELECT *  FROM `subject` WHERE `status_id`='1' ORDER BY `subject`.`subno` ASC");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("subno"));
                v.add(rs.getString("description"));
                Double dp = Double.parseDouble(rs.getString("price"));
                v.add(df.format(dp));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();

        }
    }

