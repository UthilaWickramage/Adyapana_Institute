    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String subject_id = jTextField1.getText();
        String price = jTextField2.getText();
        String desc = jTextArea1.getText();

        if (desc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter description", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("([0]|[1-9][0-9]*)|([0][.]([0]*[1-9][0-9]*))|([1-9][0-9]*[.][0-9]+)").matcher(price).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid price input", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                ResultSet rsx = MySQL.search("SELECT * FROM `subject` WHERE `subno`='" + subject_id + "' AND`status_id`='1'");

                if (rsx.next()) {
                    MySQL.iud("UPDATE `subject` SET `description`='" + desc + "' WHERE `price`='" + price + "'");
                    resetFields();
                    loadSubjects();
                    JOptionPane.showMessageDialog(this, "Subject updated successfully ", "Success", JOptionPane.INFORMATION_MESSAGE);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String subject = jTextField1.getText();
        String price = jTextField2.getText();
        String desc = jTextArea1.getText();

        if (subject.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter subject id", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("SUB[-]\\d{2}").matcher(subject).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid subject id", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (desc.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter subject description", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("([0]|[1-9][0-9]*)|([0][.]([0]*[1-9][0-9]*))|([1-9][0-9]*[.][0-9]+)").matcher(price).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid price input", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs1 = MySQL.search("SELECT * FROM `subject` WHERE `subno`='" + subject + "'");
                rs1.next();

                if (rs1.next()) {
                    JOptionPane.showMessageDialog(this, "Subject already exists", "Error", JOptionPane.ERROR_MESSAGE);

                } else {
                    MySQL.iud("INSERT INTO `subject`(`subno`,`description`,`price`,`user_id`,`status_id`) VALUES ('" + subject + "','" + desc + "','" + price + "'," + Signin.userId + ", '1')");
                    loadSubjects();
                    JOptionPane.showMessageDialog(this, "New subject added Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
                resetFields();

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectedRow = jTable1.getSelectedRow();
        if (evt.getClickCount() == 1) {
            jTextField1.setEditable(false);
            jTextField1.setText(jTable1.getValueAt(selectedRow, 0).toString());
            jTextField2.setText(jTable1.getValueAt(selectedRow, 2).toString());
            jTextArea1.setText(jTable1.getValueAt(selectedRow, 1).toString());

        }

        if (evt.getClickCount() == 2) {

            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Please select a Subject", "Error", JOptionPane.ERROR_MESSAGE);

            } else {

                int option = JOptionPane.showConfirmDialog(this, "Do you want to remove the selected subject?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

                if (option == JOptionPane.YES_OPTION) {
                    MySQL.iud("UPDATE `subject` SET `status_id`='2' WHERE `subno`='" + jTable1.getValueAt(selectedRow, 0).toString() + "'");
                    loadSubjects();
                    resetFields();

                    JOptionPane.showMessageDialog(this, "subject removed successfully", "Warning", JOptionPane.INFORMATION_MESSAGE);

                    jButton2.grabFocus();
                }

            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

