    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String class_id = jTextField1.getText();
        String day = jComboBox1.getSelectedItem().toString();
        String stime = jTimeChooser1.getFormatedTime();
        String etime = jTimeChooser2.getFormatedTime();
        String subject_id = jComboBox2.getSelectedItem().toString();
        String teacher_id = jComboBox4.getSelectedItem().toString();

        if (!Pattern.compile("C[-]\\d{2}").matcher(class_id).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid class id", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (day.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select the day", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (stime.equals("00:00:00")) {
            JOptionPane.showMessageDialog(this, "Please select start time", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (etime.equals("00:00:00")) {
            JOptionPane.showMessageDialog(this, "Please select end time", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (subject_id.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select subject", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (teacher_id.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select teacher", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            try {
                ResultSet rs1 = MySQL.search("SELECT * FROM `class` WHERE `classno`='" + class_id + "'");
                rs1.next();

                if (rs1.next()) {
                    JOptionPane.showMessageDialog(this, "Class already exists", "Error", JOptionPane.ERROR_MESSAGE);

                } else {
                    MySQL.iud("INSERT INTO `class`(`classno`,`start_time`,`end_time`,`day`,`subno`,`tno`,`user_id`,`status_id`) VALUES ('" + class_id + "','" + stime + "','" + etime + "','" + day + "','" + subject_id + "','" + teacher_id + "'," + Signin.userId + ", '1')");
                    loadClasses();
                    resetFields();
                    JOptionPane.showMessageDialog(this, "New subject added Successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

