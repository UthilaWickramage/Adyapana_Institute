    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectedRow = jTable1.getSelectedRow();
        try {
            jTextField1.setEditable(false);
            jTextField1.setText(jTable1.getValueAt(selectedRow, 0).toString());
            Date sd = new SimpleDateFormat("HH:mm:ss").parse(jTable1.getValueAt(selectedRow, 7).toString());
            Date ed = new SimpleDateFormat("HH:mm:ss").parse(jTable1.getValueAt(selectedRow, 8).toString());
            jTimeChooser1.setTime(sd);
            jTimeChooser2.setTime(ed);
            jComboBox1.setSelectedItem(jTable1.getValueAt(selectedRow, 5).toString());
            jComboBox2.setSelectedItem(jTable1.getValueAt(selectedRow, 1).toString());
            jComboBox2.setEnabled(false);
            jComboBox4.setSelectedItem(jTable1.getValueAt(selectedRow, 3).toString());
            jLabel8.setText(jTable1.getValueAt(selectedRow, 2).toString());
            jLabel18.setText(jTable1.getValueAt(selectedRow, 4).toString());

        } catch (Exception e) {
            e.printStackTrace();
        }

        if (evt.getClickCount() == 2) {

            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Please select a class", "Error", JOptionPane.ERROR_MESSAGE);

            } else {

                if (se != null) {
                    se.jLabel15.setText(jTable1.getValueAt(selectedRow, 0).toString());
                    se.jLabel19.setText(jTable1.getValueAt(selectedRow, 6).toString());
                    se.jLabel21.setText(jTable1.getValueAt(selectedRow, 7).toString());
                    se.jLabel23.setText(jTable1.getValueAt(selectedRow, 8).toString());
                    se.jLabel25.setText(jTable1.getValueAt(selectedRow, 1).toString());
                    se.jLabel27.setText(jTable1.getValueAt(selectedRow, 2).toString());
                    se.jLabel29.setText(jTable1.getValueAt(selectedRow, 4).toString());
                    se.jLabel31.setText(jTable1.getValueAt(selectedRow, 5).toString());
                    DecimalFormat df = new DecimalFormat("0.00");
                    se.jLabel34.setText(df.format(Double.parseDouble(jTable1.getValueAt(selectedRow, 3).toString())));

                    se.jButton2.setText("Change Class");
                    this.dispose();
                }

            }
        }


    }//GEN-LAST:event_jTable1MouseClicked

