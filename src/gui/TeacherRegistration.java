    public void loadTeachers() {

        try {
            ResultSet rs = MySQL.search("SELECT *  FROM `teacher` INNER JOIN `address` ON `address`.`id`=`teacher`.`address_id` INNER JOIN `city` ON `city`.`id` = `address`.`city_id` WHERE `status_id`='1' ORDER BY `teacher`.`tno` ASC");

            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);

            while (rs.next()) {
                Vector v = new Vector();
                v.add(rs.getString("tno"));
                v.add(rs.getString("name"));
                v.add(rs.getString("address.add_no"));
                v.add(rs.getString("address.street_1"));
                v.add(rs.getString("address.street_2"));
                v.add(rs.getString("city.name"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();

        }
    }

