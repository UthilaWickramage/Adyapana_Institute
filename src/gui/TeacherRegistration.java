    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        int selectedRow = jTable1.getSelectedRow();
        if (evt.getClickCount() == 1) {
            jTextField1.setEditable(false);
            jTextField1.setText(jTable1.getValueAt(selectedRow, 0).toString());
            jTextField2.setText(jTable1.getValueAt(selectedRow, 1).toString());
            jTextField3.setText(jTable1.getValueAt(selectedRow, 2).toString());
            jTextField4.setText(jTable1.getValueAt(selectedRow, 3).toString());
            jTextField5.setText(jTable1.getValueAt(selectedRow, 4).toString());
            jComboBox1.setSelectedItem(jTable1.getValueAt(selectedRow, 5).toString());
           
        }

        if (evt.getClickCount() == 2) {

            if (selectedRow == -1) {
                JOptionPane.showMessageDialog(this, "Please select a teacher", "Error", JOptionPane.ERROR_MESSAGE);

            } else {

                int option = JOptionPane.showConfirmDialog(this, "Do you want to remove the selected teacher?", "Confirmation", JOptionPane.YES_NO_OPTION, JOptionPane.WARNING_MESSAGE);

                if (option == JOptionPane.YES_OPTION) {
                    MySQL.iud("UPDATE `teacher` SET `status_id`='2' WHERE `tno`='" + jTable1.getValueAt(selectedRow, 0).toString() + "'");
                    loadTeachers();
                    resetFields();

                    JOptionPane.showMessageDialog(this, "teacher removed successfully", "Warning", JOptionPane.INFORMATION_MESSAGE);

                    jButton2.grabFocus();
                }

            }
        }
    }//GEN-LAST:event_jTable1MouseClicked

